import java.util.Scanner;

public class B10869 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int inputA = scanner.nextInt();
        int inputB = scanner.nextInt();

        System.out.println(inputA + inputB);
        System.out.println(inputA - inputB);
        System.out.println(inputA * inputB);
        System.out.println(inputA / inputB);
        System.out.println(inputA % inputB);

    }
}
